def resolve_ticker(text: str) -> str:
    """입력을 티커로 변환. 이름이면 코드로, 이미 코드면 그대로.
    매칭: 원문 → 공백제거 → 대문자 순으로 시도."""
    t = text.strip()
    # 1) 정확히 이름 매칭
    if t in NAME_TO_CODE:
        return NAME_TO_CODE[t]
    # 2) 공백 제거 후 매칭 (예: "에스케이 하이닉스")
    nospace = t.replace(" ", "")
    if nospace in NAME_TO_CODE:
        return NAME_TO_CODE[nospace]
    # 3) 대소문자 무시 (영문 이름/티커)
    upper = t.upper()
    for name, code in NAME_TO_CODE.items():
        if name.upper() == upper:
            return code
    # 4) 못 찾으면 원문 그대로 (코드 직접 입력한 경우)
    return t
